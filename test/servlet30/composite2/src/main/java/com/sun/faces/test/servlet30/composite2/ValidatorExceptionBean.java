package com.sun.faces.test.servlet30.composite2;

import static javax.faces.application.FacesMessage.SEVERITY_WARN;

import java.io.Serializable;

import javax.enterprise.context.SessionScoped;
import javax.faces.application.FacesMessage;
import javax.faces.component.UIComponent;
import javax.faces.context.FacesContext;
import javax.faces.validator.ValidatorException;
import javax.inject.Named;

@Named
@SessionScoped
public class ValidatorExceptionBean implements Serializable {

    private static final long serialVersionUID = 1L;

    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void validateName(FacesContext context, UIComponent component, Object object) throws ValidatorException {
        throw new ValidatorException(new FacesMessage(SEVERITY_WARN, "Validation failed", "Component validation failed."));
    }
}
